package fencecalc.bytribalfence;

public class Customer {

    String name;
    String phoneNumber;
    String email;
    String street;
    String houseNumber;
    String city;


    public Customer(String name, String phoneNumber, String email, String street, String houseNumber, String city) {
        this.name = name;
        this.phoneNumber = phoneNumber;
        this.email = email;
        this.street = street;
        this.houseNumber = houseNumber;
        this.city = city;
    }
}
